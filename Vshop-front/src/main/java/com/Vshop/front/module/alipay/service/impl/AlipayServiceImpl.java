	/**
	 * 拼接订单body
	 * @param orderId
	 * @return
	 */
	public String getBody(List<Order> orderlist){
		String body = "无数据";
		if(orderlist.size()>0){
			String name = "";
			for(Order order:orderlist){
				List<OrderGoods> goodslist=order.getOrderGoodsList();
				if(goodslist.size()>0){
					for(OrderGoods ordergoods:goodslist){
						name+=ordergoods.getGoodsName();
					}
				}
			}
			body = name;
		}
		return body;
	}

